    private void ttableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ttableMouseClicked
        int row = ttable.getSelectedRow();
        txtname.setText(mod.getValueAt(row, 1).toString());
        String sex = mod.getValueAt(row, 2).toString();
        if (sex.equals("Male")) {
            rmale.setSelected(true);
            rfemale.setSelected(false);
        } else {
            rmale.setSelected(false);
            rfemale.setSelected(true);
        }
        txtdate.setText(mod.getValueAt(row, 3).toString());
        String subject = mod.getValueAt(row, 4).toString();
        switch (subject) {
            case "C++": {
                txtsub.setSelectedIndex(0);
            }
            break;
            case "Java": {
                txtsub.setSelectedIndex(1);
            }
            break;
            case "PHP":
                txtsub.setSelectedIndex(2);
                break;
            case "Python":
                txtsub.setSelectedIndex(3);
                break;

        }
        txttime.setText(mod.getValueAt(row, 5).toString());
        txtphone.setText(mod.getValueAt(row, 6).toString());

    }//GEN-LAST:event_ttableMouseClicked

