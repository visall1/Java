    private void SetTable() {
        try {
            sql = "SELECT * FROM stu_info";
            ps = connection().prepareStatement(sql);
            r = ps.executeQuery();
            while (r.next()) {
                mod = (DefaultTableModel) ttable.getModel();
                String id = r.getString(1);
                String name = r.getString(2);
                String sex = r.getString(3);
                String date = r.getString(4);
                String subject = r.getString(5);
                String time = r.getString(6);
                String phone = r.getString(7);
                Object[] row = {id, name, sex, date, subject, time, phone};
                mod.addRow(row);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }

