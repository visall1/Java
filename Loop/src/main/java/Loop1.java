public class Loop1 {
    public static void main(String[] args){
        int i;
        System.out.println("For Loop Statement 1");
        for(i=1;i<=10;i++){
            System.out.print(i+ " ");
        }
        System.out.println("\nFor Loop Statement 2");
        for(i=10;i>=1;i--){
            System.out.print(i+ " ");
        }
    }
}